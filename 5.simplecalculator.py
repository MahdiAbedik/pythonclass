

while True :
    num1 = float(input("lotfan adad aval ra vared konid :")) 
    num2 = float(input("lotfan adad dovom ra vared konid :"))

    operation = input("lotfan amalgar khod ra vared konid :")
    if operation == "+":
       natije = num1 + num2
       print(natije)
    elif operation == "-":
        natije = num1 - num2
        print(natije)    
    elif operation == "*":
        natije = num1 * num2
        print(natije)
    elif operation == "/":
         natije = num1 / num2
         print(natije)



